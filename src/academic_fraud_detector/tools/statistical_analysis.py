# 3. GRIM Test
# ═══════════════════════════════════════════════════════════════════════

class GRIMTestTool(BaseTool):
    """
    GRIM (Granularity-Related Inconsistency of Means) Test.

    For integer-scale data with small sample sizes, reported means are
    mathematically constrained: given N integer observations, the mean
    can only take certain fractional values.

    Specifically: mean × N must be an integer (or very close to one).
    If mean × N is not an integer, the reported mean is MATHEMATICALLY IMPOSSIBLE
    given the sample size — definitive evidence of error or fabrication.

    Example:
    - N=20, reported mean=3.45 → 3.45 × 20 = 69.0 → CONSISTENT ✓
    - N=20, reported mean=3.47 → 3.47 × 20 = 69.4 → INCONSISTENT ✗

    Reference: Brown & Heathers (2017). "The GRIM Test."
    """

    name: str = "grim_test"
    description: str = (
        "Apply the GRIM (Granularity-Related Inconsistency of Means) test. "
        "For integer-scale data with known sample sizes, checks whether reported "
        "means are mathematically possible. An inconsistent mean is DEFINITIVE proof "
        "of error/fabrication — it's a mathematical impossibility, not a statistical "
        "likelihood. Use on: means from Likert scales, counts, test scores, or any "
        "data where individual observations must be integers. "
        "Input: list of {'mean': float, 'n': int} pairs."
    )
    args_schema: type[BaseModel] = MeanSamplePairsInput

    def _run(self, pairs: List[dict], data_description: str = "") -> str:
        """Execute GRIM test on (mean, N) pairs."""
        import numpy as np

        if not pairs:
            return json.dumps({
                "test": "GRIM Test",
                "error": "No (mean, N) pairs provided.",
                "flagged": False,
            })

        results = []
        inconsistent_count = 0

        for i, pair in enumerate(pairs):
            mean = pair.get("mean")
            n = pair.get("n")

            if mean is None or n is None:
                results.append({
                    "pair_index": i,
                    "error": "Missing 'mean' or 'n' key.",
                })
                continue

            if not isinstance(n, int) or n <= 0:
                results.append({
                    "pair_index": i,
                    "mean": mean,
                    "n": n,
                    "error": "Sample size n must be a positive integer.",
                })
                continue

            # Compute: mean × N should be integer (or very close)
            product = mean * n
            nearest_integer = round(product)
            deviation = abs(product - nearest_integer)

            # Allow tiny floating-point imprecision
            is_consistent = deviation < 0.01
            if not is_consistent:
                inconsistent_count += 1

            results.append({
                "pair_index": i,
                "mean": mean,
                "n": n,
                "product": round(product, 4),
                "nearest_integer": nearest_integer,
                "deviation": round(deviation, 6),
                "consistent": is_consistent,
                "detail": (
                    f"CONSISTENT: {mean} × {n} = {product:.4f} ≈ {nearest_integer}"
                    if is_consistent
                    else f"INCONSISTENT: {mean} × {n} = {product:.4f} ≠ any integer. "
                         f"Deviation = {deviation:.4f}. This mean is MATHEMATICALLY IMPOSSIBLE "
                         f"for N={n} integer observations."
                ),
            })

        flagged = inconsistent_count > 0

        return json.dumps({
            "test": "GRIM (Granularity-Related Inconsistency of Means)",
            "pairs_tested": len(results),
            "consistent_pairs": len(results) - inconsistent_count,
            "inconsistent_pairs": inconsistent_count,
            "flagged": flagged,
            "results": results,
            "data_description": data_description,
            "interpretation": (
                f"Found {inconsistent_count} mathematically impossible mean(s). "
                "This is DEFINITIVE evidence of data fabrication"
                if flagged
                else "All reported means are mathematically consistent."
            ),
        }, ensure_ascii=False)


