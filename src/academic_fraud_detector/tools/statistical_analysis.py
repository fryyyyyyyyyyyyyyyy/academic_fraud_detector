# 1. Benford's Law Test
# ═══════════════════════════════════════════════════════════════════════

class BenfordLawTool(BaseTool):
    """
    Test whether numeric data conforms to Benford's Law (First-Digit Law).

    Benford's Law states that in naturally-occurring numeric data, the leading digit
    'd' appears with probability log10(1 + 1/d). This property holds for data that
    spans multiple orders of magnitude — regression coefficients, financial data,
    population counts, physical constants, etc.

    Fabricated data often deviates significantly because humans:
    - Have digit preferences (e.g., favor 3 and 7)
    - Create numbers that are too uniformly distributed
    - Under-produce leading digit 1 (should be ~30%)

    Statistical method: Pearson's chi-squared goodness-of-fit test.
    p < 0.05 suggests possible fabrication.

    IMPORTANT: Benford's Law is NOT applicable to:
    - Data with fixed bounds (e.g., percentages, grades)
    - Data generated by sequential numbering (e.g., invoice numbers)
    - Very small datasets (n < 30)
    """

    name: str = "benford_law_test"
    description: str = (
        "Test numeric data against Benford's Law (First-Digit distribution). "
        "Fabricated data often deviates from Benford's expected digit frequencies. "
        "Applies chi-squared goodness-of-fit test. p < 0.05 flags possible fabrication. "
        "Use on: regression coefficients, means, SDs, F-statistics, total scores, "
        "count data spanning multiple orders of magnitude. "
        "DO NOT use on: bounded data (percentages, Likert scales), sequential IDs, "
        "or datasets with fewer than 30 values."
    )
    args_schema: type[BaseModel] = NumericDataInput

    def _run(self, data_values: List[float], data_description: str = "") -> str:
        """Execute Benford's Law test."""
        import numpy as np
        from scipy import stats

        # Extract first non-zero digits
        first_digits = []
        for x in data_values:
            if x == 0 or np.isnan(x) or np.isinf(x):
                continue
            x_abs = abs(x)
            # Handle scientific notation / very small numbers
            if x_abs < 1.0 and x_abs > 0:
                # Get first significant digit
                digit_str = f"{x_abs:.10f}".lstrip("0.")
                first_nonzero = next((c for c in digit_str if c != "0"), None)
                if first_nonzero:
                    first_digits.append(int(first_nonzero))
            else:
                digit_str = str(int(x_abs))
                if digit_str:
                    first_digits.append(int(digit_str[0]))

        if len(first_digits) < 30:
            return json.dumps({
                "test": "Benford's Law",
                "error": f"Insufficient data (n={len(first_digits)}, need ≥ 30).",
                "flagged": False,
                "sample_size": len(first_digits),
            })

        # Benford expected probabilities
        benford_probs = np.array([np.log10(1 + 1 / d) for d in range(1, 10)])
        observed_counts = np.array([first_digits.count(d) for d in range(1, 10)])

        # Chi-squared test
        chi2, p_value = stats.chisquare(f_obs=observed_counts, f_exp=benford_probs * len(first_digits))

        # Mean Absolute Deviation (MAD) — alternative metric
        observed_props = observed_counts / observed_counts.sum()
        mad = np.mean(np.abs(observed_props - benford_probs))

        # Conformity thresholds (Nigrini, 2012):
        # MAD < 0.006: close conformity
        # MAD 0.006-0.012: acceptable conformity
        # MAD 0.012-0.015: marginally acceptable
        # MAD > 0.015: non-conformity
        mad_assessment = (
            "close conformity" if mad < 0.006 else
            "acceptable conformity" if mad < 0.012 else
            "marginally acceptable conformity" if mad < 0.015 else
            "non-conformity — suspicious"
        )

        flagged = bool(p_value < 0.05 or mad > 0.015)

        return json.dumps({
            "test": "Benford's Law — Chi-Squared Goodness-of-Fit",
            "sample_size": int(len(first_digits)),
            "chi_squared": round(_to_python(chi2), 4),
            "degrees_of_freedom": 8,
            "p_value": round(_to_python(p_value), 6),
            "mean_absolute_deviation": round(_to_python(mad), 6),
            "mad_assessment": mad_assessment,
            "observed_distribution": {
                str(d): int(c) for d, c in zip(range(1, 10), observed_counts)
            },
            "expected_benford_distribution": {
                str(d): round(_to_python(p), 4) for d, p in zip(range(1, 10), benford_probs)
            },
            "flagged": flagged,
            "data_description": data_description,
            "interpretation": (
                f"Data significantly deviates from Benford's Law (p={_to_python(p_value):.4f}, MAD={_to_python(mad):.4f}). "
                "This is a statistical red flag for potential data fabrication."
                if flagged
                else "Data conforms to Benford's Law — no fabrication signal from digit distribution."
            ),
        }, ensure_ascii=False)


