class AnomalousPrecisionTool(BaseTool):
    """
    Detect statistical signatures of fabricated data that is "too perfect."

    Detection dimensions:
    1. SD/mean ratio: flag CV < 1% (critical) or < 3% (warning)
    2. Last-digit bias: humans fabricating numbers favor 0 and 5
       - Separate checks for "last digit = 0", "last digit = 5", and "0+5 combined"
    3. Near-arithmetic progression: check if consecutive values in ordered series
       have suspiciously constant differences (CV of diffs < threshold)
    4. High-frequency repeated values: check if any value appears suspiciously often
       in supposedly independent measurements
    5. Excessive significance rate: >80% p-values significant
    6. P-hacking clustering: too many p-values in [0.01, 0.05)
    """

    name: str = "anomalous_precision_check"
    description: str = (
        "Detect data that is 'too perfect to be true' — implausibly small standard "
        "deviations, biased last-digit distributions (separate 0 and 5 checks), "
        "near-arithmetic progressions (编造数据常呈现近似等差), "
        "high-frequency repeated values (独立实验中同一数值多次出现), "
        "excessive concentration of significant p-values. "
        "Well-established signatures of fabricated data. "
        "Provide means, SDs, sample sizes, p-values, raw values, "
        "and ordered_series_json for arithmetic progression detection."
    )
    args_schema: type[BaseModel] = AnomalousPrecisionInput

    def _run(
        self,
        means: Optional[List[float]] = None,
        sds: Optional[List[float]] = None,
        sample_sizes: Optional[List[int]] = None,
        p_values: Optional[List[float]] = None,
        raw_values: Optional[List[float]] = None,
        ordered_series_json: str = "[]",
        data_description: str = "",
    ) -> str:
        """Execute anomalous precision checks."""
        import json as _json

        means = means or []
        sds = sds or []
        p_values = p_values or []
        raw_values = raw_values or []

        # Parse ordered series
        ordered_series = []
        try:
            parsed = _json.loads(ordered_series_json)
            if isinstance(parsed, list):
                ordered_series = [
                    s for s in parsed
                    if isinstance(s, list) and len(s) >= 3
                ]
        except (_json.JSONDecodeError, TypeError):
            ordered_series = []

        findings = []
        checks_run = []
        total_checks = 0
        flagged_checks = 0

        # ── 1. SD/Mean ratio check ──
        if len(means) > 0 and len(sds) > 0 and len(means) == len(sds):
            checks_run.append("sd_mean_ratio")
            suspicious_sds = []
            for i, (m, sd) in enumerate(zip(means, sds)):
                if m != 0 and sd >= 0:
                    cv = abs(sd / m)
                    if cv < 0.01:
                        suspicious_sds.append({
                            "index": i, "mean": m, "sd": sd,
                            "cv": round(cv, 4), "severity": "critical",
                            "detail": f"CV={cv*100:.2f}% — implausibly precise.",
                        })
                        flagged_checks += 1
                    elif cv < 0.03:
                        suspicious_sds.append({
                            "index": i, "mean": m, "sd": sd,
                            "cv": round(cv, 4), "severity": "warning",
                            "detail": f"CV={cv*100:.2f}% — unusually low.",
                        })
                        flagged_checks += 1
            total_checks += len(means)
            if suspicious_sds:
                findings.append({
                    "check": "sd_mean_ratio",
                    "finding": f"{len(suspicious_sds)}/{len(means)} mean-SD pairs "
                               f"have suspiciously low CV.",
                    "details": suspicious_sds,
                })

        # ── 2. Last-digit preference analysis (强化版：独立检测末位0和末位5) ──
        if len(raw_values) >= 10:
            checks_run.append("last_digit_analysis")
            last_digits = []
            for v in raw_values:
                v_str = f"{abs(v):.10g}"
                if "." in v_str:
                    frac = v_str.split(".")[1]
                    if frac:
                        last_digits.append(int(frac[-1]))
                elif v_str[-1].isdigit():
                    last_digits.append(int(v_str[-1]))

            if len(last_digits) >= 10:
                from collections import Counter
                digit_counts = Counter(last_digits)
                total = len(last_digits)
                digit_freqs = {
                    str(d): round(digit_counts.get(d, 0) / total, 3)
                    for d in range(10)
                }
                biased = {d: round(f, 3) for d, f in digit_freqs.items()
                          if float(f) > 0.20}

                # 独立检测末位=0和末位=5
                freq_0 = digit_freqs.get("0", 0)
                freq_5 = digit_freqs.get("5", 0)
                zero_five = freq_0 + freq_5
                expected_per_digit = 0.10  # uniform expectation

                digit_flags = []
                # 末位=5 独立检测（编造数据中5是最高频的偏好数字之一）
                if freq_5 > 0.20:
                    digit_flags.append(
                        f"末位=5 占比 {freq_5:.1%}（期望 10%），"
                        f"是期望值的 {freq_5/0.10:.1f} 倍"
                    )
                    flagged_checks += 1
                elif freq_5 > 0.15:
                    digit_flags.append(
                        f"末位=5 占比 {freq_5:.1%}（期望 10%），轻度偏高"
                    )
                    flagged_checks += 1

                # 末位=0 独立检测
                if freq_0 > 0.20:
                    digit_flags.append(
                        f"末位=0 占比 {freq_0:.1%}（期望 10%），"
                        f"是期望值的 {freq_0/0.10:.1f} 倍"
                    )
                    flagged_checks += 1

                # 组合检测
                if zero_five > 0.35:
                    digit_flags.append(
                        f"末位0+5合计占比 {zero_five:.1%}（期望 20%）"
                    )
                    if not digit_flags:
                        flagged_checks += 1

                # 其他偏好的数字
                if biased:
                    if not digit_flags:
                        flagged_checks += 1

                total_checks += 1
                if digit_flags or biased:
                    findings.append({
                        "check": "last_digit_analysis",
                        "finding": "末位数字分布异常。 " + "；".join(digit_flags) if digit_flags else (
                            f"Over-represented digits: {biased}."
                        ),
                        "digit_frequencies": digit_freqs,
                        "last_digit_5_frequency": freq_5,
                        "last_digit_0_frequency": freq_0,
                        "zero_five_combined": round(zero_five, 3),
                        "overrepresented_digits": biased,
                        "n_analyzed": total,
                        "interpretation": (
                            "人为编造的数据通常呈现末位数字偏好，"
                            "尤其是末位=5（如 1.5, 2.5, 3.5...）和末位=0，"
                            "因为人类倾向于选择'整齐'或'中间'的数字。"
                        ),
                    })

        # ── 2.5. 近等差数列检测 (Near-Arithmetic Progression Detection) ──
        # 编造数据最常见的模式：逐组递减/递增固定量
        # 真实数据相邻差值波动大，编造数据差值高度一致
        if ordered_series:
            checks_run.append("near_arithmetic_progression")
            progression_flags = []
            for si, series in enumerate(ordered_series):
                if len(series) < 3:
                    continue
                vals = np.array(series, dtype=np.float64)
                # 计算相邻差值
                diffs = np.diff(vals)
                diff_mean = float(np.mean(np.abs(diffs)))
                diff_std = float(np.std(diffs))

                if diff_mean < 1e-10:
                    # 所有值完全相同 → 由 cross_figure_data_compare 处理
                    continue

                # 核心指标：差值的变异系数 CV_diff = std(diffs) / |mean(diffs)|
                # CV_diff 越小 → 差值越一致 → 越像等差数列
                cv_diff = float(diff_std / diff_mean) if diff_mean > 1e-10 else 0.0

                # 完美等差数列：CV_diff = 0
                # 近似等差数列：CV_diff < 0.10（差值波动不到均值的10%）
                # 可疑：CV_diff < 0.20

                severity = None
                if cv_diff < 0.05:
                    severity = "critical"
                elif cv_diff < 0.10:
                    severity = "high"
                elif cv_diff < 0.20:
                    severity = "medium"

                if severity:
                    # 计算如果差值是完美等差数列的拟合优度
                    n = len(vals)
                    # 完美等差: y = a + b*x where b = (last-first)/(n-1)
                    perfect_step = (vals[-1] - vals[0]) / (n - 1)
                    perfect_vals = np.array([
                        vals[0] + perfect_step * k for k in range(n)
                    ])
                    residuals = np.abs(vals - perfect_vals)
                    max_residual = float(np.max(residuals))
                    # R² of fit to perfect arithmetic progression
                    ss_res = float(np.sum((vals - perfect_vals) ** 2))
                    ss_tot = float(np.sum((vals - np.mean(vals)) ** 2))
                    r_squared = float(1 - ss_res / ss_tot) if ss_tot > 1e-10 else 1.0

                    progression_flags.append({
                        "series_index": si,
                        "values": [round(float(v), 4) for v in vals],
                        "n_values": n,
                        "diffs": [round(float(d), 4) for d in diffs],
                        "diff_mean": round(diff_mean, 4),
                        "diff_std": round(diff_std, 4),
                        "cv_of_diffs": round(cv_diff, 6),
                        "perfect_step": round(float(perfect_step), 4),
                        "max_residual_from_perfect": round(max_residual, 6),
                        "r_squared_to_perfect_ap": round(r_squared, 6),
                        "severity": severity,
                        "interpretation": (
                            f"该序列的相邻差值变异系数 CV={cv_diff:.4f}，"
                            f"R² 对完美等差数列 = {r_squared:.6f}。"
                            f"差值: {[round(float(d), 2) for d in diffs]}。"
                            + (
                                f"差值近乎恒定（CV={cv_diff:.4f}），"
                                f"呈现典型的近似等差数列特征 — "
                                f"这是人为编造数据的最强信号之一。"
                                f"真实生物实验数据几乎不可能出现差值如此一致的序列。"
                                if cv_diff < 0.10
                                else f"差值较为一致（CV={cv_diff:.4f}），"
                                f"存在人为编造数据的可能性。"
                            )
                        ),
                    })

            total_checks += len(ordered_series)
            if progression_flags:
                # Count as flagged per series with flags
                flagged_checks += len(progression_flags)
                findings.append({
                    "check": "near_arithmetic_progression",
                    "finding": (
                        f"{len(progression_flags)}/{len(ordered_series)} 个有序序列"
                        f"呈现近等差数列特征。"
                    ),
                    "progression_details": progression_flags,
                    "interpretation": (
                        "近等差数列（相邻差值高度一致）是人为编造数据的经典指纹。"
                        "真实实验数据由于生物变异和测量误差，"
                        "相邻数据点的差值几乎不可能高度一致。"
                        "差值变异系数 CV_diff < 0.10 时高度可疑，"
                        "< 0.05 时几乎可以确定是编造的。"
                    ),
                })

        # ── 2.6. 高频重复值检测 (High-Frequency Repeated Values) ──
        if len(raw_values) >= 6:
            checks_run.append("high_frequency_repeats")
            from collections import Counter as _Counter
            val_counter = _Counter(raw_values)
            n_unique = len(val_counter)
            n_total = len(raw_values)

            # 如果一个值在数据集中出现 ≥3 次且占比 > 20%，高度可疑
            # 多个"独立"实验不应该产生完全相同的测量值
            repeat_flags = []
            for val, count in val_counter.most_common():
                freq = count / n_total
                if count >= 3 and freq > 0.15:
                    repeat_flags.append({
                        "value": val,
                        "count": count,
                        "frequency": round(freq, 4),
                        "severity": "critical" if count >= 4 and freq > 0.25 else "high",
                        "interpretation": (
                            f"数值 {val} 在数据集中出现 {count} 次 "
                            f"（占比 {freq:.1%}）。"
                            + (
                                f"这在声称的独立实验中极不合理 "
                                f"— 真实独立测量产生完全相同数值的概率极低。"
                                if count >= 4
                                else "可能不是独立实验产生。"
                            )
                        ),
                    })

            total_checks += 1
            if repeat_flags:
                flagged_checks += 1
                findings.append({
                    "check": "high_frequency_repeats",
                    "finding": (
                        f"发现 {len(repeat_flags)} 个数值在数据集中高频重复出现，"
                        f"与独立实验的预期不符。"
                    ),
                    "repeat_details": repeat_flags,
                    "dataset_summary": {
                        "total_values": n_total,
                        "unique_values": n_unique,
                        "uniqueness_ratio": round(n_unique / n_total, 4),
                    },
                    "interpretation": (
                        "真实独立实验由于生物变异、测量误差和随机因素，"
                        "几乎不可能产生完全相同的测量值。"
                        "同一数值多次出现（≥3次且占比>15%）说明数据可能被复制粘贴。"
                    ),
                })

        # ── 3. P-value significance concentration ──
        if len(p_values) >= 5:
            checks_run.append("significance_concentration")
            sig_count = sum(1 for p in p_values if p < 0.05)
            just_sig = sum(1 for p in p_values if 0.01 <= p < 0.05)
            total_p = len(p_values)
            sig_rate = sig_count / total_p if total_p > 0 else 0

            total_checks += 1
            if sig_rate > 0.80:
                flagged_checks += 1
                findings.append({
                    "check": "significance_concentration",
                    "finding": f"{sig_count}/{total_p} ({sig_rate:.1%}) p-values "
                               f"significant — unusually high rate.",
                    "significant_rate": round(sig_rate, 3),
                })

            if just_sig >= 3 and just_sig / max(total_p, 1) > 0.4:
                flagged_checks += 1
                findings.append({
                    "check": "p_hacking_clustering",
                    "finding": f"{just_sig}/{total_p} p-values cluster in [0.01, 0.05) — "
                               f"classic p-hacking signature.",
                })

        return json.dumps({
            "test": "Anomalous Precision & Fabrication Indicators",
            "data_description": data_description,
            "checks_run": checks_run,
            "total_checks": total_checks,
            "flagged_checks": flagged_checks,
            "flagged": flagged_checks > 0,
            "findings": findings,
            "interpretation": (
                f"{flagged_checks}/{total_checks} checks flagged. "
                + ("Suspicious patterns detected." if flagged_checks > 0
                   else "No anomalous precision indicators detected.")
            ),
        }, ensure_ascii=False)


