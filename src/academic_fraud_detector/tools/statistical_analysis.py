"""
Statistical analysis tools — detect data fabrication through forensic statistics.

Three core tests:
1. Benford's Law: checks if leading digits follow expected distribution.
   Fabricated numbers often deviate because humans are bad at generating random digits.
2. P-value Distribution (Caliper Test): checks for anomalous clustering of p-values
   just below 0.05 — the classic "p-hacking" signature.
3. GRIM Test: checks whether reported means are mathematically consistent with
   reported sample sizes for integer-scale data.

These are the most technically rigorous tools in the system — the mathematical
foundations are well-established in forensic statistics literature.
"""

import json
import logging
from typing import List, Optional, Any

import numpy as np
from crewai.tools import BaseTool
from pydantic import BaseModel, Field

logger = logging.getLogger(__name__)


def _to_python(val: Any) -> Any:
    """Convert numpy scalar to Python native type for JSON serialization."""
    if isinstance(val, (np.integer,)):
        return int(val)
    if isinstance(val, (np.floating,)):
        return float(val)
    if isinstance(val, (np.bool_,)):
        return bool(val)
    if isinstance(val, np.ndarray):
        return val.tolist()
    return val


