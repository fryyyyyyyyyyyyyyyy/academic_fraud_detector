# 2. P-value Distribution / Caliper Test
# ═══════════════════════════════════════════════════════════════════════

class PValueDistributionTool(BaseTool):
    """
    Analyze the distribution of reported p-values for fabrication signatures.

    Under the null hypothesis, p-values should be uniformly distributed on [0, 1].
    When there are true effects, a spike near zero appears — but the rest should
    still be roughly uniform.

    Suspicious patterns:
    1. CLUSTERING JUST BELOW 0.05: The "caliper test" — if significantly more
       p-values fall in (0.045, 0.05] than in (0.05, 0.055], this suggests
       p-hacking / rounding / selective reporting.
    2. NON-UNIFORM DISTRIBUTION: Overall deviation from uniformity suggests
       the p-values were not generated by genuine statistical tests.
    3. GAPS at specific thresholds: missing values just above 0.05 or 0.01.

    Reference: Simonsohn, Nelson & Simmons (2014). "P-Curve: A Key to the File-Drawer."
    """

    name: str = "p_value_distribution_test"
    description: str = (
        "Analyze the distribution of reported p-values for anomalies. "
        "Checks for p-hacking signatures: clustering just below 0.05 (caliper test), "
        "non-uniform distribution, and gaps at significance thresholds. "
        "Use this on ALL p-values reported in the paper. "
        "Input: list of p-values extracted from the paper text and tables."
    )
    args_schema: type[BaseModel] = NumericDataInput

    def _run(self, data_values: List[float], data_description: str = "") -> str:
        """Execute p-value distribution analysis."""
        import numpy as np
        from scipy import stats

        # Clean: keep only valid p-values
        pvalues = np.array([p for p in data_values if 0 < p <= 1], dtype=np.float64)

        if len(pvalues) < 10:
            return json.dumps({
                "test": "P-value Distribution Analysis",
                "error": f"Insufficient p-values (n={len(pvalues)}, need ≥ 10).",
                "flagged": False,
                "p_value_count": len(pvalues),
            })

        findings = []

        # ── 1. Caliper Test (clustering just below 0.05) ──
        just_below_05 = int(np.sum((pvalues > 0.045) & (pvalues <= 0.05)))
        just_above_05 = int(np.sum((pvalues > 0.05) & (pvalues <= 0.055)))
        caliper_ratio = just_below_05 / max(just_above_05, 1) if just_above_05 > 0 else float('inf')

        if caliper_ratio > 2.0:
            findings.append({
                "type": "caliper_test",
                "detail": (
                    f"Discontinuity at p=0.05: {just_below_05} values in (0.045, 0.05] "
                    f"vs {just_above_05} in (0.05, 0.055]. Ratio = {caliper_ratio:.2f}."
                ),
                "suspicious": True,
            })

        # Also check 0.01 threshold
        just_below_01 = int(np.sum((pvalues > 0.009) & (pvalues <= 0.01)))
        just_above_01 = int(np.sum((pvalues > 0.01) & (pvalues <= 0.011)))
        if just_below_01 > 2 * max(just_above_01, 1):
            findings.append({
                "type": "caliper_test_001",
                "detail": (
                    f"Discontinuity at p=0.01: {just_below_01} values just below vs "
                    f"{just_above_01} just above."
                ),
                "suspicious": True,
            })

        # ── 2. Uniformity Test ──
        hist, bin_edges = np.histogram(pvalues, bins=10, range=(0, 1))
        expected_per_bin = len(pvalues) / 10
        chi2_uniform, p_uniform = stats.chisquare(
            f_obs=hist,
            f_exp=[expected_per_bin] * 10,
        )

        if p_uniform < 0.05:
            findings.append({
                "type": "uniformity_deviation",
                "detail": (
                    f"P-value distribution deviates from uniformity (χ²={chi2_uniform:.2f}, "
                    f"p={p_uniform:.4f})."
                ),
                "suspicious": True,
            })

        # ── 3. Summary Statistics ──
        median_p = float(np.median(pvalues))
        below_05_pct = float(np.mean(pvalues <= 0.05) * 100)

        # ── 4. Overall Assessment ──
        suspicious_count = len([f for f in findings if f.get("suspicious")])

        # Base suspicion on number of failed tests AND the caliper ratio
        flagged = bool(suspicious_count >= 1 or caliper_ratio > 2.0)

        # Serialize caliper_ratio: use a very large number for infinity
        caliper_ratio_serialized = (
            round(float(caliper_ratio), 4) if caliper_ratio != float('inf') else 999.0
        )

        return json.dumps({
            "test": "P-value Distribution Analysis",
            "p_value_count": int(len(pvalues)),
            "p_value_range": [round(float(pvalues.min()), 4), round(float(pvalues.max()), 4)],
            "median_p_value": round(float(median_p), 4),
            "percent_below_0.05": round(float(below_05_pct), 1),
            "caliper_test": {
                "p_values_in_0.045_to_0.05": int(just_below_05),
                "p_values_in_0.05_to_0.055": int(just_above_05),
                "caliper_ratio": caliper_ratio_serialized,
                "caliper_ratio_is_infinite": caliper_ratio == float('inf'),
                "interpretation": (
                    "Anomalous clustering just below 0.05 — potential p-hacking or selective reporting."
                    if caliper_ratio > 2.0
                    else "Normal distribution around the 0.05 threshold."
                ),
            },
            "uniformity_test": {
                "chi_squared": round(_to_python(chi2_uniform), 4),
                "p_value": round(_to_python(p_uniform), 6),
                "interpretation": (
                    "P-value distribution significantly deviates from uniformity."
                    if _to_python(p_uniform) < 0.05
                    else "P-value distribution is consistent with uniformity."
                ),
            },
            "histogram": {
                f"[{round(float(bin_edges[i]), 1)}, {round(float(bin_edges[i+1]), 1)})": int(hist[i])
                for i in range(len(hist))
            },
            "findings": findings,
            "flagged": flagged,
            "data_description": data_description,
            "interpretation": (
                f"Found {suspicious_count} suspicious pattern(s) in p-value distribution. "
                "This is a statistical red flag for p-hacking or selective reporting."
                if flagged
                else "P-value distribution appears normal — no p-hacking signal detected."
            ),
        }, ensure_ascii=False)


