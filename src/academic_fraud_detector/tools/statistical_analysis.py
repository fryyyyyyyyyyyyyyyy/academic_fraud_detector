# Statistical Consistency Verification
# ═══════════════════════════════════════════════════════════════════════

class StatisticalConsistencyInput(BaseModel):
    """Input for statistical consistency verification."""

    test_statistics: str = Field(
        default="[]",
        description=(
            "JSON list of test statistic objects. Each object: "
            '"test_type" ("t","F","chi2","r"), '
            '"value" (test statistic), "df" or "df1"/"df2", '
            '"reported_p" (optional), "reported_n" (optional). '
            'Example: [{"test_type":"t","value":2.45,"df":28,"reported_p":0.021}]'
        ),
    )
    data_description: str = Field(default="", description="Data source description.")


class StatisticalConsistencyTool(BaseTool):
    """
    Verify internal consistency of reported statistical results.

    1. t/F/chi2 consistency: given stat and df, verify reported p-value
    2. df vs N consistency: df should match N-1 (one-sample) or N-2 (two-sample)
    3. Impossible values: negative F/chi2, |r| > 1
    """

    name: str = "statistical_consistency_check"
    description: str = (
        "Verify reported statistical results are internally consistent. "
        "Checks if t/F/chi2 value with reported df gives the reported p-value, "
        "if df matches sample size, and if any values are mathematically impossible. "
        "Fabricators often invent inconsistent (stat, df, p) triplets."
    )
    args_schema: type[BaseModel] = StatisticalConsistencyInput

    def _run(
        self,
        test_statistics: str = "[]",
        data_description: str = "",
    ) -> str:
        """Execute statistical consistency checks."""
        try:
            stats_list = json.loads(test_statistics)
            if not isinstance(stats_list, list):
                return json.dumps({
                    "error": "test_statistics must be a JSON list.", "flagged": False,
                })
        except json.JSONDecodeError:
            return json.dumps({
                "error": "test_statistics must be valid JSON.", "flagged": False,
            })

        if not stats_list:
            return json.dumps({
                "test": "Statistical Consistency Verification",
                "data_description": data_description,
                "statistics_checked": 0,
                "flagged": False,
                "findings": [],
                "interpretation": "No test statistics provided.",
            })

        from scipy import stats as scipy_stats

        findings = []
        flagged_count = 0
        results = []

        for i, s in enumerate(stats_list):
            test_type = s.get("test_type", "").lower()
            value = s.get("value")
            df = s.get("df")
            df1 = s.get("df1")
            df2 = s.get("df2")
            reported_p = s.get("reported_p")
            reported_n = s.get("reported_n")

            result = {"index": i, "test_type": test_type, "value": value, "issues": []}

            # Basic validity
            if value is not None:
                if test_type == "F" and value < 0:
                    result["issues"].append("F-statistic cannot be negative.")
                if test_type == "chi2" and value < 0:
                    result["issues"].append("Chi-squared cannot be negative.")
                if test_type == "r" and abs(value) > 1:
                    result["issues"].append("|r| > 1 is impossible.")

            # P-value consistency
            if value is not None and reported_p is not None:
                try:
                    computed_p = None
                    if test_type == "t" and df is not None:
                        computed_p = float(2 * scipy_stats.t.sf(abs(value), df))
                    elif test_type == "F" and df1 is not None and df2 is not None:
                        computed_p = float(scipy_stats.f.sf(value, df1, df2))
                    elif test_type == "chi2" and df is not None:
                        computed_p = float(scipy_stats.chi2.sf(value, df))

                    if computed_p is not None:
                        result["computed_p"] = round(computed_p, 6)
                        result["reported_p"] = reported_p
                        ratio = (
                            max(computed_p, reported_p) / min(computed_p, reported_p)
                            if min(computed_p, reported_p) > 1e-300 else float("inf")
                        )
                        if ratio > 10:
                            result["issues"].append(
                                f"p mismatch: reported={reported_p}, "
                                f"computed={computed_p:.6f} (ratio={ratio:.1f}x)"
                            )
                            flagged_count += 1
                except Exception:
                    pass

            # df vs N
            if df is not None and reported_n is not None and test_type == "t":
                exp1 = reported_n - 1
                exp2 = reported_n - 2 if reported_n >= 2 else None
                if df != exp1 and (exp2 is None or df != exp2):
                    result["issues"].append(
                        f"df={df} vs N={reported_n}: expected df≈{exp1} "
                        f"(one-sample) or df≈{exp2} (two-sample)"
                    )

            if result["issues"]:
                flagged_count += 1
                findings.append(result)
            results.append(result)

        return json.dumps({
            "test": "Statistical Consistency Verification",
            "data_description": data_description,
            "statistics_checked": len(stats_list),
            "flagged": flagged_count > 0,
            "inconsistent_count": flagged_count,
            "findings": findings,
            "all_results": results,
            "interpretation": (
                f"{flagged_count}/{len(stats_list)} test statistic(s) internally "
                "inconsistent — strong evidence of fabrication."
                if flagged_count > 0
                else "All checked test statistics are internally consistent."
            ),
        }, ensure_ascii=False)


